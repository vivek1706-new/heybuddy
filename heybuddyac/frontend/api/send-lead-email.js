import { Resend } from 'resend';

// Initialize with Vercel environment variable
const resend = new Resend(process.env.RESEND_API_KEY);

export default async function handler(req, res) {
    // Only accept POST requests
    if (req.method !== 'POST') {
        return res.status(405).json({ error: 'Method not allowed' });
    }

    const { dealerEmails, leadDetails, buyerPhone } = req.body;

    if (!dealerEmails || dealerEmails.length === 0) {
        return res.status(400).json({ error: 'No dealer emails provided.' });
    }

    // ─── BEAUTIFUL BRANDED EMAIL TEMPLATE ──────────────────────────────
    const htmlTemplate = `
        <div style="font-family: 'Inter', -apple-system, sans-serif; max-width: 600px; margin: 0 auto; background-color: #0F1218; padding: 40px; border-radius: 12px; color: #FFFFFF; border: 1px solid #1C212B;">
            <div style="text-align: center; margin-bottom: 30px;">
                <h1 style="color: #FFB347; margin: 0; font-size: 28px; letter-spacing: -0.5px;">HeyBuddy</h1>
                <p style="color: #94A3B8; font-size: 14px; margin-top: 5px; text-transform: uppercase; letter-spacing: 2px;">Premium Marketplace Lead</p>
            </div>
            
            <div style="background-color: #161B22; padding: 25px; border-radius: 8px; border-left: 4px solid #FFB347; margin-bottom: 25px;">
                <h2 style="margin-top: 0; color: #FFFFFF; font-size: 20px;">🎉 New Validated Lead Assigned to You!</h2>
                <p style="color: #E2E8F0; line-height: 1.6; font-size: 15px;">You have a fresh hyper-local requirement waiting for your quote because they are inside your preferred radius. Respond quickly to win the customer!</p>
            </div>

            <table style="width: 100%; border-collapse: collapse; margin-bottom: 30px; font-size: 15px;">
                <tr>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #94A3B8;">Category</td>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #FFFFFF; font-weight: 600; text-align: right;">${leadDetails.category}</td>
                </tr>
                <tr>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #94A3B8;">Brand Requested</td>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #FFFFFF; font-weight: 600; text-align: right;">${leadDetails.brand}</td>
                </tr>
                <tr>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #94A3B8;">Location Match</td>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #FFFFFF; font-weight: 600; text-align: right;">${leadDetails.location}</td>
                </tr>
                <tr>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #94A3B8;">Buyer Identity</td>
                    <td style="padding: 16px 0; border-bottom: 1px solid #1C212B; color: #10B981; font-weight: bold; text-align: right;">✅ Verified +91-${buyerPhone}</td>
                </tr>
            </table>

            <div style="text-align: center; margin-top: 40px;">
                <a href="https://heybuddy-marketplace.vercel.app/agent" style="background-color: #FFB347; color: #0F1218; padding: 16px 36px; text-decoration: none; font-weight: 800; border-radius: 8px; font-size: 16px; display: inline-block;">Open Inbox to Send Quote</a>
            </div>
            
            <div style="margin-top: 50px; text-align: center; color: #475569; font-size: 12px; border-top: 1px solid #1C212B; padding-top: 20px;">
                This message is an automated fulfillment alert generated by the HeyBuddy Algorithm.<br/>
                © 2026 HeyBuddy Technologies. All rights reserved.
            </div>
        </div>
    `;

    try {
        // Send the email to the array of dealers
        const { data, error } = await resend.emails.send({
            from: 'HeyBuddy Alerts <no-reply@heybuddy.chat>', // Using your newly verified custom domain!
            to: dealerEmails, // Array of emails or a single email string
            subject: '🚨 New Lead Match: ' + leadDetails.category + ' in ' + leadDetails.location,
            html: htmlTemplate,
        });

        if (error) {
            return res.status(400).json({ success: false, error });
        }

        res.status(200).json({ success: true, data });
    } catch (error) {
        res.status(500).json({ success: false, error: "Internal Error" });
    }
}
